            <p>Entreprises tout d’abord - PME comme groupes - mais aussi institutionnels, écoles, startups innovantes ou encore partenaires du développement des entreprises, ils seront une vingtaine d’intervenants de premier plan pour vous donner une image des défis et des opportunités qui attendent notre industrie.</p>
