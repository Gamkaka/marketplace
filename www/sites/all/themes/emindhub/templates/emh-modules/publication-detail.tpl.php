            <p>Mercredi 9 novembre prochain aura lieu à Paris la première édition des Rencontres pour la Compétivité Industrielle, à l’initiative d’OPEO, et avec le soutien de Bpifrance et du magazine Décideurs.</p>
            <p>« Yes we Fab ! » </p>
